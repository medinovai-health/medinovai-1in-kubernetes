  base_image: "medinovai/base:latest"
  security_scan: true
  vulnerability_check: true
  
build:
  multi_stage: true
  optimization: true
  cache_strategy: "layer"
  
deployment:
  auto_deploy: false
  approval_required: true
  rollback_enabled: true
EOF

    # Create data-services-config.yml
    cat > "$medinovai_dir/data-services-config.yml" << EOF
# MedinovAI Data Services Integration Configuration
# Generated by MedinovAI Standards Migration Script

data_services:
  required: true
  endpoint: "https://data-services.medinovai.com"
  authentication: "oauth2"
  
