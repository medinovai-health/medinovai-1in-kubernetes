  performance_target: "sub-second"
  
timeouts:
  dynamic: true
  base_timeout: 300
  max_timeout: 1800
  min_timeout: 60
  
monitoring:
  health_checks: true
  metrics_collection: true
  logging_level: "INFO"
  
compliance:
  hipaa: true
  gdpr: true
  soc2: true
  fda: false
EOF

    # Create registry-config.yml
    cat > "$medinovai_dir/registry-config.yml" << EOF
# MedinovAI Registry Integration Configuration
# Generated by MedinovAI Standards Migration Script

