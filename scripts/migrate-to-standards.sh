#!/bin/bash
# MedinovAI Standards Migration Script
# Migrates repositories to comply with MedinovAI Comprehensive Standards v2.0.0

set -euo pipefail

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
TEMPLATES_DIR="$PROJECT_ROOT/templates"
BACKUP_DIR="$PROJECT_ROOT/migration-backups/$(date +%Y%m%d_%H%M%S)"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Logging functions
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Create backup directory
create_backup() {
    local repo_path="$1"
    local repo_name=$(basename "$repo_path")
    local backup_path="$BACKUP_DIR/$repo_name"
    
    log_info "Creating backup for $repo_name..."
    mkdir -p "$backup_path"
    cp -r "$repo_path" "$backup_path/"
    log_success "Backup created at $backup_path"
}

# Update Dockerfile to use MedinovAI base image
update_dockerfile() {
    local repo_path="$1"
    local dockerfile_path="$repo_path/Dockerfile"
    
    log_info "Updating Dockerfile for $(basename "$repo_path")..."
    
    if [ -f "$dockerfile_path" ]; then
        # Backup original Dockerfile
        cp "$dockerfile_path" "$dockerfile_path.backup"
        
        # Create new Dockerfile with MedinovAI standards
        cat > "$dockerfile_path" << 'EOF'
# MedinovAI Standards Compliant Dockerfile
# Generated by MedinovAI Standards Migration Script

FROM medinovai/base:latest

# Security: Run as non-root user
USER medinovai:medinovai

# Set working directory
WORKDIR /app

# Copy application code
COPY --chown=medinovai:medinovai . /app

# Install Python dependencies (if requirements.txt exists)
RUN if [ -f requirements.txt ]; then \
        pip install --no-cache-dir --user -r requirements.txt; \
    fi

# Install Node.js dependencies (if package.json exists)
RUN if [ -f package.json ]; then \
        npm ci --only=production; \
    fi

# Health check
HEALTHCHECK --interval=30s --timeout=10s --start-period=5s --retries=3 \
    CMD curl -f http://localhost:8080/health || exit 1

# Expose port
EXPOSE 8080

# Start application
CMD ["python", "app.py"]
EOF
        
        log_success "Dockerfile updated"
    else
        log_warning "No Dockerfile found, creating new one..."
        cp "$TEMPLATES_DIR/dockerfile-standards/Dockerfile" "$dockerfile_path"
    fi
}

# Create .medinovai directory structure
create_medinovai_config() {
    local repo_path="$1"
    local medinovai_dir="$repo_path/.medinovai"
    
    log_info "Creating .medinovai configuration for $(basename "$repo_path")..."
    
    mkdir -p "$medinovai_dir"
    
    # Create standards.yml
    cat > "$medinovai_dir/standards.yml" << EOF
# MedinovAI Repository Standards Configuration
# Generated by MedinovAI Standards Migration Script

repository:
  name: "$(basename "$repo_path")"
  type: "service"
  version: "1.0.0"
  
standards:
  registry_integration: true
  data_services_required: true
