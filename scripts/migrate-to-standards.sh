# Main migration function
migrate_repository() {
    local repo_path="$1"
    local repo_name=$(basename "$repo_path")
    
    log_info "Starting migration for repository: $repo_name"
    
    # Validate repository path
    if [ ! -d "$repo_path" ]; then
        log_error "Repository path does not exist: $repo_path"
        return 1
    fi
    
    # Create backup
    create_backup "$repo_path"
    
    # Apply migrations
    create_medinovai_config "$repo_path"
    update_dockerfile "$repo_path"
    add_cicd_workflows "$repo_path"
    add_health_check "$repo_path"
    add_monitoring_config "$repo_path"
    
    log_success "Migration completed for: $repo_name"
}

# Validate migration
validate_migration() {
    local repo_path="$1"
    local repo_name=$(basename "$repo_path")
    
    log_info "Validating migration for: $repo_name"
    
    local validation_passed=true
    
    # Check required files
    local required_files=(
        ".medinovai/standards.yml"
        ".medinovai/registry-config.yml"
        ".medinovai/data-services-config.yml"
        ".github/workflows/ci.yml"
        ".github/workflows/cd.yml"
        ".github/workflows/standards-validation.yml"
        "Dockerfile"
        "src/health.py"
    )
    
    for file in "${required_files[@]}"; do
        if [ ! -f "$repo_path/$file" ]; then
            log_error "Missing required file: $file"
            validation_passed=false
        fi
    done
    
    if [ "$validation_passed" = true ]; then
        log_success "Migration validation passed for: $repo_name"
    else
        log_error "Migration validation failed for: $repo_name"
    fi
    
    return $([ "$validation_passed" = true ] && echo 0 || echo 1)
}

# Main execution
main() {
    log_info "MedinovAI Standards Migration Script v2.0.0"
    log_info "Starting migration process..."
    
    # Create backup directory
    mkdir -p "$BACKUP_DIR"
    log_info "Backup directory created: $BACKUP_DIR"
    
    # Find all MedinovAI repositories
    local repos_found=0
    local repos_migrated=0
    local repos_failed=0
    
    for repo in /Users/dev1/github/medinovai-*; do
        if [ -d "$repo" ]; then
            repos_found=$((repos_found + 1))
            log_info "Found repository: $(basename "$repo")"
            
            if migrate_repository "$repo"; then
                if validate_migration "$repo"; then
                    repos_migrated=$((repos_migrated + 1))
                else
                    repos_failed=$((repos_failed + 1))
                fi
            else
                repos_failed=$((repos_failed + 1))
            fi
        fi
    done
    
    # Summary
    log_info "Migration Summary:"
    log_info "  Repositories found: $repos_found"
    log_info "  Successfully migrated: $repos_migrated"
    log_info "  Failed migrations: $repos_failed"
    log_info "  Backup location: $BACKUP_DIR"
    
    if [ $repos_failed -eq 0 ]; then
        log_success "All repositories migrated successfully!"
        exit 0
    else
        log_error "Some migrations failed. Check logs for details."
        exit 1
    fi
}

# Run main function
main "$@"

