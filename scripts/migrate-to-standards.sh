  read_only: false
  write_operations: true
  batch_operations: true
  
compliance:
  encryption_at_rest: true
  encryption_in_transit: true
  audit_logging: true
  
performance:
  connection_pooling: true
  caching_enabled: true
  timeout_dynamic: true
EOF

    log_success ".medinovai configuration created"
}

# Add CI/CD workflows
add_cicd_workflows() {
    local repo_path="$1"
    local workflows_dir="$repo_path/.github/workflows"
    
    log_info "Adding CI/CD workflows for $(basename "$repo_path")..."
    
    mkdir -p "$workflows_dir"
    
    # Create CI workflow
    cat > "$workflows_dir/ci.yml" << EOF
name: Continuous Integration

on:
  push:
    branches: [ main, develop ]
  pull_request:
    branches: [ main ]

jobs:
  test:
    runs-on: ubuntu-latest
    
    steps:
    - uses: actions/checkout@v4
    
    - name: Set up Python
      uses: actions/setup-python@v4
      with:
        python-version: '3.11'
    
    - name: Install dependencies
      run: |
        python -m pip install --upgrade pip
        pip install -r requirements.txt
    
    - name: Run tests
      run: |
        python -m pytest tests/ -v
    
    - name: Run linting
      run: |
        flake8 src/
        black --check src/
    
    - name: Security scan
      run: |
        bandit -r src/
        safety check

  build:
    needs: test
    runs-on: ubuntu-latest
    
    steps:
    - uses: actions/checkout@v4
    
    - name: Build Docker image
      run: |
        docker build -t medinovai/$(basename "$repo_path"):latest .
    
    - name: Security scan
      run: |
        docker run --rm -v /var/run/docker.sock:/var/run/docker.sock \\
          aquasec/trivy image medinovai/$(basename "$repo_path"):latest
EOF

    # Create CD workflow
    cat > "$workflows_dir/cd.yml" << EOF
name: Continuous Deployment

on:
  push:
    branches: [ main ]

jobs:
  deploy:
    runs-on: ubuntu-latest
    
    steps:
    - uses: actions/checkout@v4
    
    - name: Build and push to registry
      run: |
        echo \${{ secrets.REGISTRY_TOKEN }} | docker login medinovai-registry.com -u \${{ secrets.REGISTRY_USERNAME }} --password-stdin
        docker build -t medinovai/$(basename "$repo_path"):\${{ github.sha }} .
        docker push medinovai/$(basename "$repo_path"):\${{ github.sha }}
        docker push medinovai/$(basename "$repo_path"):latest
    
    - name: Deploy to Kubernetes
      run: |
        kubectl set image deployment/$(basename "$repo_path") $(basename "$repo_path")=medinovai/$(basename "$repo_path"):\${{ github.sha }}
EOF

    # Create standards validation workflow
    cat > "$workflows_dir/standards-validation.yml" << EOF
name: Standards Validation

on:
  push:
    branches: [ main, develop ]
  pull_request:
    branches: [ main ]

jobs:
  validate-standards:
    runs-on: ubuntu-latest
    
    steps:
    - uses: actions/checkout@v4
    
    - name: Validate MedinovAI Standards
      run: |
        python scripts/validate-standards.py
    
    - name: Check registry integration
      run: |
        test -f .medinovai/registry-config.yml
        test -f .medinovai/data-services-config.yml
        test -f .medinovai/standards.yml
    
    - name: Validate Dockerfile
      run: |
        docker run --rm -i hadolint/hadolint < Dockerfile
EOF

    log_success "CI/CD workflows added"
}

# Add health check endpoint
add_health_check() {
    local repo_path="$1"
    local src_dir="$repo_path/src"
    
    log_info "Adding health check endpoint for $(basename "$repo_path")..."
    
    mkdir -p "$src_dir"
    
    # Create health check module
    cat > "$src_dir/health.py" << EOF
"""
MedinovAI Health Check Module
Compliant with MedinovAI Standards v2.0.0
"""

import time
import psutil
from datetime import datetime
from typing import Dict, Any
from medinovai_monitoring import HealthChecker

class ServiceHealthChecker(HealthChecker):
    def __init__(self, service_name: str):
        super().__init__()
        self.service_name = service_name
        self.start_time = time.time()
    
    def check_health(self) -> Dict[str, Any]:
        """Comprehensive health check"""
        health_status = {
            "status": "healthy",
            "timestamp": datetime.utcnow().isoformat(),
            "service": self.service_name,
            "uptime": time.time() - self.start_time,
            "checks": {}
        }
        
        # Check system resources
        health_status["checks"]["system"] = self._check_system_resources()
        
        # Check service dependencies
        health_status["checks"]["dependencies"] = self._check_dependencies()
        
        # Determine overall status
        if any(check["status"] != "healthy" for check in health_status["checks"].values()):
            health_status["status"] = "unhealthy"
        
        return health_status
    
    def _check_system_resources(self) -> Dict[str, Any]:
        """Check system resource usage"""
        try:
            cpu_percent = psutil.cpu_percent(interval=1)
            memory = psutil.virtual_memory()
            
            return {
                "status": "healthy" if cpu_percent < 90 and memory.percent < 90 else "warning",
                "cpu_usage": cpu_percent,
                "memory_usage": memory.percent,
                "available_memory": memory.available
            }
        except Exception as e:
            return {
                "status": "error",
                "error": str(e)
            }
    
    def _check_dependencies(self) -> Dict[str, Any]:
        """Check service dependencies"""
        # This should be implemented based on specific service dependencies
        return {
            "status": "healthy",
            "dependencies": []
        }
EOF

    log_success "Health check endpoint added"
}

