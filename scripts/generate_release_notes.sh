- [ ] Adding documentation
- [ ] Adding monitoring configurations

### Medium Risk Changes
- [ ] Modifying existing CI/CD pipelines
- [ ] Changing deployment configurations
- [ ] Updating security policies
- [ ] Modifying ingress configurations

### High Risk Changes
