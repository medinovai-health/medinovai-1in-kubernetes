# Function to commit changes
commit_changes() {
    local repo_name="$1"
    local repo_path="$2"
    
    log_info "Committing changes for $repo_name..."
    
    cd "$repo_path"
    
    # Add all changes
    git add .
    
    # Check if there are changes to commit
    if git diff --cached --quiet; then
        log_warning "No changes to commit for $repo_name"
        cd - > /dev/null
        return 0
    fi
    
    # Commit with standardized message
    git commit -m "feat: MedinovAI deployment v$VERSION_PREFIX

- Complete infrastructure deployment
- AI/ML models integration (9 models)
- Monitoring and observability setup
- Security and testing framework
- GitOps workflow implementation
- Agent Swarms parallel deployment

Closes: MedinovAI deployment initiative
Release: $VERSION_PREFIX"
    
    log_success "Changes committed for $repo_name"
    cd - > /dev/null
}

# Function to merge branches to main
merge_branches() {
    local repo_name="$1"
    local repo_path="$2"
    
    log_info "Merging branches to main for $repo_name..."
    
    cd "$repo_path"
    
    # Get all remote branches
    git fetch origin
    
    # Get list of branches to merge (excluding main and HEAD)
    local branches_to_merge=$(git branch -r | grep -v "origin/main" | grep -v "origin/HEAD" | sed 's/origin\///' | tr -d ' ')
    
    if [ -z "$branches_to_merge" ]; then
        log_info "No branches to merge for $repo_name"
        cd - > /dev/null
        return 0
    fi
    
    # Merge each branch
    for branch in $branches_to_merge; do
        log_info "Merging branch: $branch"
        
        # Check if branch exists locally
        if git show-ref --verify --quiet "refs/heads/$branch"; then
            git checkout "$branch"
            git pull origin "$branch"
        else
            git checkout -b "$branch" "origin/$branch"
        fi
        
        # Switch back to main
        git checkout main
        
        # Merge branch
        if git merge "$branch" --no-ff -m "Merge branch '$branch' into main

- Merging $branch into main for MedinovAI deployment
- Version: $VERSION_PREFIX
- Date: $(date)"; then
            log_success "Successfully merged $branch into main"
        else
            log_warning "Merge conflict in $branch, skipping..."
            git merge --abort
        fi
    done
    
    # Ensure we're on main
    git checkout main
    
    log_success "Branch merging completed for $repo_name"
    cd - > /dev/null
}

# Function to create release
create_release() {
    local repo_name="$1"
    local repo_path="$2"
    
    log_info "Creating release for $repo_name..."
    
    cd "$repo_path"
    
    # Push all changes
    git push origin main --tags
    
    # Create GitHub release if gh CLI is available
    if command -v gh &> /dev/null; then
        gh release create "$VERSION_PREFIX" \
            --title "MedinovAI Deployment $VERSION_PREFIX" \
            --notes-file "$RELEASE_NOTES_FILE" \
            --latest
        log_success "GitHub release created for $repo_name"
    else
        log_warning "GitHub CLI not available, release created locally only"
    fi
    
    cd - > /dev/null
}

# Function to process single repository
process_repository() {
    local repo_name="$1"
    local repo_path="repositories/$repo_name"
    
    log_info "Processing repository: $repo_name"
    
    # Clone repository if it doesn't exist
    if [ ! -d "$repo_path" ]; then
        log_info "Cloning repository: $repo_name"
        git clone "https://github.com/$MEDINOVAI_ORG/$repo_name.git" "$repo_path"
    fi
    
    # Update repository
    cd "$repo_path"
    git fetch origin
    git checkout main
    git pull origin main
    cd - > /dev/null
    
    # Process repository
    create_restore_point "$repo_name" "$repo_path"
    generate_release_notes "$repo_name" "$repo_path"
    commit_changes "$repo_name" "$repo_path"
    merge_branches "$repo_name" "$repo_path"
    create_release "$repo_name" "$repo_path"
    
    log_success "Completed processing: $repo_name"
}

# Main execution
main() {
    log_info "Starting MedinovAI Repository Commit & Release Process"
    log_info "Version: $VERSION_PREFIX"
    log_info "Restore Point Tag: $RESTORE_POINT_TAG"
    log_info "Log File: $LOG_FILE"
    
    # Create repositories directory
    mkdir -p repositories
    
    # Get repository list
    get_repositories
    
    # Process each repository
    while IFS= read -r repo_name; do
        if [ -n "$repo_name" ]; then
            process_repository "$repo_name"
            echo "---" | tee -a "$LOG_FILE"
        fi
    done < medinovai_repos_list.txt
    
    log_success "All repositories processed successfully!"
    log_info "Check $LOG_FILE for detailed logs"
}

# Run main function
main "$@"
