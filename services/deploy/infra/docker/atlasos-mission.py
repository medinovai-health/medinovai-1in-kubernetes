    log("1/7 medinovai-registry (triple-quote Dockerfile)")
    results["medinovai-registry"] = fix_registry()

    log("2/7 medinovai-data-services (missing shared package)")
    results["medinovai-data-services"] = fix_data_services()

    log("3/7 medinovai-real-time-stream-bus (wrong Dockerfile language)")
    results["medinovai-real-time-stream-bus"] = fix_stream_bus()

