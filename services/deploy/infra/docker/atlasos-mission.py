def fix_registry():
    """Fix medinovai-registry Dockerfile (starts with ''')"""
    repo = REPOS_BASE / "medinovai-registry"
    df_path = repo / "Dockerfile"
    content = df_path.read_text()
    if content.startswith("'''"):
        # Find first FROM line
        lines = content.split('\n')
        for i, line in enumerate(lines):
            if line.strip().startswith('FROM'):
                fixed = '\n'.join(lines[i:])
                (repo / "Dockerfile.TEST2").write_text(fixed)
                log("medinovai-registry: fixed triple-quote Dockerfile")
                return docker_build(f"{REG}/medinovai-registry:latest", str(repo), str(repo / "Dockerfile.TEST2"))
    return docker_build(f"{REG}/medinovai-registry:latest", str(repo))

def fix_data_services():
    """Fix medinovai-data-services — ensure shared/medinovai-core exists"""
    repo = REPOS_BASE / "medinovai-data-services"
    shared = repo / "shared" / "medinovai-core"
    core_repo = REPOS_BASE / "medinovai-core"
    if not shared.exists() and core_repo.exists():
        shutil.copytree(str(core_repo), str(shared))
        log("medinovai-data-services: copied medinovai-core to shared/")
    elif not shared.exists():
        # Create minimal stub package
        shared.mkdir(parents=True)
        (shared / "setup.py").write_text("from setuptools import setup\nsetup(name='medinovai-core', version='1.0.0', packages=[])\n")
        (shared / "medinovai_core").mkdir(exist_ok=True)
        (shared / "medinovai_core" / "__init__.py").write_text("# MedinovAI Core\n")
    return docker_build(f"{REG}/medinovai-data-services:latest", str(repo))

