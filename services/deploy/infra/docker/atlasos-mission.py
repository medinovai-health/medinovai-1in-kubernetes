# ─── AI Service Generator ─────────────────────────────────────────────────────

def generate_service(service_name: str, description: str) -> bool:
    """Use Ollama to generate a real minimal FastAPI service implementation."""
    log(f"Generating {service_name} with AI...")

    system_prompt = """You are an expert Python/FastAPI developer.
Generate minimal but real FastAPI services for healthcare platforms.
Services must:
- Have proper health endpoint at GET /health
- Have proper root GET / endpoint
- Have at least 2-3 domain-specific API endpoints based on the service purpose
- Use proper Pydantic models
- Be production-ready (error handling, logging)
- Listen on PORT env var (default 8080)
Output ONLY the complete Python code for main.py, nothing else."""

    user_prompt = f"""Create a FastAPI service for: {service_name}
Description: {description}

Requirements:
1. GET /health → {{"status": "ok", "service": "{service_name}"}}
2. GET /api/status → operational status
3. 2-3 domain endpoints specific to the service purpose with proper request/response models
4. Uses structlog or logging for audit trails
5. Port from os.environ.get("PORT", "8080")
6. CORS middleware enabled

Write complete main.py code only."""

    code = ask_ai(user_prompt, system_prompt)

    if not code or len(code) < 100:
        log(f"AI generation failed for {service_name}, using template", "WARN")
        code = generate_template(service_name, description)

    # Clean up code blocks if present
    if "```python" in code:
        code = code.split("```python")[1].split("```")[0].strip()
    elif "```" in code:
        code = code.split("```")[1].split("```")[0].strip()

    # Write the service
    build_dir = BUILD_DIR / service_name
    build_dir.mkdir(parents=True, exist_ok=True)
    (build_dir / "main.py").write_text(code)
    (build_dir / "requirements.txt").write_text(
        "fastapi>=0.104.0\nuvicorn[standard]>=0.24.0\npydantic>=2.0.0\n"
        "structlog>=23.0.0\naiohttp>=3.9.0\n"
    )
    (build_dir / "Dockerfile").write_text(textwrap.dedent(f"""
        FROM python:3.12-slim
        WORKDIR /app
        RUN apt-get update && apt-get install -y --no-install-recommends curl && rm -rf /var/lib/apt/lists/*
        COPY requirements.txt .
        RUN pip install --no-cache-dir -r requirements.txt
        COPY main.py .
        ENV SERVICE_NAME={service_name} PORT=8080
        EXPOSE 8080
        HEALTHCHECK --interval=30s --timeout=10s --start-period=30s --retries=3 \\
          CMD curl -sf http://localhost:8080/health || exit 1
        CMD ["uvicorn", "main:app", "--host", "0.0.0.0", "--port", "8080"]
    """).strip())

    success = docker_build(f"{REG}/{service_name}:latest", str(build_dir))
    if success:
        log(f"  ✓ Generated and built {service_name}")
    return success

