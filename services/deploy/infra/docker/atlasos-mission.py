# ─── Logging ──────────────────────────────────────────────────────────────────

def log(msg: str, level: str = "INFO"):
    ts = time.strftime("%H:%M:%S")
    print(f"[{ts}] [{level}] {msg}", flush=True)

def log_section(title: str):
    print(f"\n{'='*70}", flush=True)
    print(f"  {title}", flush=True)
    print(f"{'='*70}\n", flush=True)

# ─── Ollama AI ─────────────────────────────────────────────────────────────────

def ask_ai(prompt: str, system: str = None, model: str = MODEL) -> str:
    """Call Ollama to generate code/text."""
    payload = {
        "model": model,
        "prompt": prompt,
        "stream": False,
        "options": {"temperature": 0.1, "num_predict": 4096}
    }
    if system:
        payload["system"] = system

    try:
        data = json.dumps(payload).encode()
        req = urllib.request.Request(
            f"{OLLAMA_HOST}/api/generate",
            data=data,
            headers={"Content-Type": "application/json"},
            method="POST"
        )
        with urllib.request.urlopen(req, timeout=120) as resp:
            result = json.loads(resp.read())
            return result.get("response", "")
    except Exception as e:
        log(f"Ollama error: {e}", "WARN")
        return ""

# ─── Docker Helpers ───────────────────────────────────────────────────────────

def run(cmd: List[str], cwd: str = None, capture: bool = True) -> tuple[int, str]:
    """Run a shell command."""
    result = subprocess.run(
        cmd, cwd=cwd, capture_output=capture,
        text=True, timeout=600
    )
    return result.returncode, (result.stdout + result.stderr)

def docker_build(tag: str, context: str, dockerfile: str = None) -> bool:
    """Build a Docker image."""
    cmd = ["docker", "build", "-t", tag]
    if dockerfile:
        cmd += ["-f", dockerfile]
    cmd.append(context)
    rc, out = run(cmd)
    if rc != 0:
        log(f"Build FAILED for {tag}\n{out[-2000:]}", "ERROR")
    return rc == 0

def docker_tag(src: str, dst: str) -> bool:
    rc, _ = run(["docker", "tag", src, dst])
    return rc == 0

